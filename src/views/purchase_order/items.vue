<template>
  <v-data-table
    :headers="headers"
    :items="itemspo"
    sort-by="calories"
    class="elevation-1"
  >
    <template v-slot:top>
      <v-toolbar
        flat
      >
        <v-toolbar-title>Items</v-toolbar-title>
        <v-divider
          class="mx-4"
          inset
          vertical
        ></v-divider>
        <v-spacer></v-spacer>
        <v-dialog
          v-model="dialog"
          max-width="1200px"
        >
          <template v-slot:activator="{ on, attrs }">
            <v-btn
              color="primary"
              dark
              class="mb-2"
              v-bind="attrs"
              v-on="on"
            >
              Add Items
            </v-btn>
          </template>
          <v-card>
             <v-app-bar
      color="primary"
      dark
      dense 
    >           <span class="headline">{{ formTitle }}</span>
                   </v-app-bar>

            <v-card-text>
              <v-container>
                <v-card-title>
      <v-text-field
        v-model="search"
        append-icon="mdi-magnify"
        label="Search"
        single-line
        hide-details
      ></v-text-field>
    </v-card-title>
    <v-data-table
      :headers="headers"
      :items="desserts"
      :search="search"
    ></v-data-table>
              </v-container>
            </v-card-text>

            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn
                color="blue darken-1"
                text
                @click="close"
              >
                Cancel
              </v-btn>
              <v-btn
                color="blue darken-1"
                text
                @click="save"
              >
                Save
              </v-btn>
            </v-card-actions>
          </v-card>
        </v-dialog>
        <v-dialog v-model="dialogDelete" max-width="500px">
          <v-card>
            <v-card-title class="headline">Apakah anda yakin akan menghapus item?</v-card-title>
            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn color="blue darken-1" text @click="closeDelete">Cancel</v-btn>
              <v-btn color="blue darken-1" text @click="deleteItemConfirm">OK</v-btn>
              <v-spacer></v-spacer>
            </v-card-actions>
          </v-card>
        </v-dialog>
      </v-toolbar>
    </template>
    <template v-slot:[`item.actions`]="{ item }">
      <v-icon
        small
        class="mr-2"
        @click="editItem(item)"
      >
        mdi-pencil
      </v-icon>
      <v-icon
        small
        @click="deleteItem(item)"
      >
        mdi-delete
      </v-icon>
    </template>
    <template v-slot:no-data>
      <v-btn
        color="primary"
        @click="initialize"
      >
        Reset
      </v-btn>
    </template>
  </v-data-table>
</template>
<script>
  export default {
    data: () => ({
      dialog: false,
      dialogDelete: false,
      headers: [
        {
          text: 'Gudang',
          align: 'start',
          sortable: false,
          value: 'gudang',
        },
        { text: 'Barang', value: 'barang' },
        { text: 'Nama', value: 'nama' },
        { text: 'PartNumber1', value: 'partnumber1' },
        { text: 'Merk', value: 'merk' },
        { text: 'Kendaraan', value: 'kendaraan' },
        { text: 'Keterangan', value: 'keterangan' },
        { text: 'ETA', value: 'eta' },
        { text: 'Jumlah', value: 'jumlah' },
        { text: 'Satuan', value: 'satuan' },
        { text: 'Harga', value: 'harga' },
        { text: 'Diskon', value: 'diskon' },
        { text: 'Sub Total', value: 'subtotal' },
        { text: 'Actions', value: 'actions', sortable: false },
      ],
      itemspo: [],
      editedIndex: -1,
      editedItem: {
        gudang: null,
        barang: null,
        nama: null,
        partnumber1: null,
        merk: null,
        keterangan: null,
        kendaraan: null,
        eta: null,
        jumlah: null,
        satuan: null,
        harga:null,
        diskon: null,
        subtotal: null

      },
      defaultItem: {
        name: '',
        calories: 0,
        fat: 0,
        carbs: 0,
        protein: 0,
      },
    }),

    computed: {
      formTitle () {
        return this.editedIndex === -1 ? 'New Item' : 'Edit Item'
      },
    },

    watch: {
      dialog (val) {
        val || this.close()
      },
      dialogDelete (val) {
        val || this.closeDelete()
      },
    },

    created () {
      this.initialize()
    },

    methods: {
      initialize () {
        this.itemspo = [
         
        ]
      },

      editItem (item) {
        this.editedIndex = this.itemspo.indexOf(item)
        this.editedItem = Object.assign({}, item)
        this.dialog = true
      },

      deleteItem (item) {
        this.editedIndex = this.itemspo.indexOf(item)
        this.editedItem = Object.assign({}, item)
        this.dialogDelete = true
      },

      deleteItemConfirm () {
        this.itemspo.splice(this.editedIndex, 1)
        this.closeDelete()
      },

      close () {
        this.dialog = false
        this.$nextTick(() => {
          this.editedItem = Object.assign({}, this.defaultItem)
          this.editedIndex = -1
        })
      },

      closeDelete () {
        this.dialogDelete = false
        this.$nextTick(() => {
          this.editedItem = Object.assign({}, this.defaultItem)
          this.editedIndex = -1
        })
      },

      save () {
        if (this.editedIndex > -1) {
          Object.assign(this.itemspo[this.editedIndex], this.editedItem)
        } else {
          this.itemspo.push(this.editedItem)
        }
        this.close()
      },
    },
  }
</script>